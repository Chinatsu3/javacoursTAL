package devoirs.arthurFabienneMartin.bibliotheque;

public class Livre {

	String titre;
	String auteur;
	String nb_tome;
	String date;
	String resume;
	
	Livre(String titre,String auteur,String nb_tome,String date, String resume){
		this.titre = titre;
		this.auteur = auteur;
		this.nb_tome = nb_tome;
		this.date = date;
		this.resume = resume;
	}
}//livre
